class Create:
	def __init__(self, posx, posy, dimw, dimh, kind):
		self.posx = posx
		self.posy = posy
		self.dimw = dimw
		self.dimh = dimh
		self.kind = kind

	def getPosx(self):
		return self.posx
	def setPosx(self):
		self.posx = posx
	def getPosy(self):
		return self.posy
	def setPosy(self):
		self.posy = posy
	def getDimw(self):
		return self.dimw
	def setDimw(self):
		self.dimw = dimw
	def getDimh(self):
		return self.dimh
	def setDimh(self):
		self.dimh = dimh
	def getKind(self):
		return self.kind
	def setKind(self):
		self.kind = kind
